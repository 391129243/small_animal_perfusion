package com.gidi.bio_console.bean;

public class BileMsgBean {

	String liverName;
	int bileCount;
	String time;
	public String getLiverName() {
		return liverName;
	}
	public void setLiverName(String liverName) {
		this.liverName = liverName;
	}
	public int getBileCount() {
		return bileCount;
	}
	public void setBileCount(int bileCount) {
		this.bileCount = bileCount;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
